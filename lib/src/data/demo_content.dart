List<StudyUnit> _makeUnits(String prefix, List<_UnitSeed> seeds) {
  return List.generate(
    seeds.length,
    (index) => StudyUnit(
      id: '${prefix}_u${index + 1}',
      title: 'Unit ${index + 1}: ${seeds[index].title}',
      order: index + 1,
      words: _buildWords(prefix, index + 1, seeds[index]),
    ),
  );
}

List<WordEntry> _buildWords(String prefix, int unit, _UnitSeed seed) {
  return List.generate(seed.words.length, (index) {
    final item = seed.words[index];
    return WordEntry(
      id: '${prefix}_u${unit}_w$index',
      en: item.en,
      ru: item.ru,
      kz: item.kz,
      definitionA1: 'A useful word about ${seed.title.toLowerCase()}: ${item.en}.',
      example: 'In Unit $unit we use the word "${item.en}" in context.',
    );
  });
}

